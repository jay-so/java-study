package part4;


public class ArihOP {
    public static void main(String[] args){
       int num1 = 7;
       int num2 = 3;

       System.out.println("num1 + num2 = " + (num1 + num2));
       System.out.println("num - num2 = " + (num1 - num2));
       System.out.println("num * num2 = " + (num1 * num2));
       System.out.println("num / num2 = " + (num1 / num2));
       System.out.println("num % num2 = " + (num1 % num2));
       //실수형 나눗셈
       System.out.println("num1 / num2 =  " + (7.0/3.0));
    }
}
