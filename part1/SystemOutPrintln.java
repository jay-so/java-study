package part1;

/*
   자바 프로그램의 출력 예시
 */
public class SystemOutPrintln {
    public static void main(String[] args) {
        System.out.println(7); //숫자
        System.out.println(3.15); //숫자
        System.out.println("3 + 5 = " + 8); //문자 + 숫자
        System.out.println(3.15 + "는 실수입니다."); //숫자 + 문자
        System.out.println("3 + 5" + "의 연산 결과는 8입니다."); //문자 + 문자
        System.out.println(3 + 5); // 숫자 + 숫자 = 덧셈 연산
    }
}
